//---------------------------------------------------------------------------
uint8 gs_is_ntsc(void)
{
	if(*((char *)0x1FC80000 - 0xAE) != 'E') return(1);
	return(0);
}

//---------------------------------------------------------------------------
uint8 gs_is_pal(void)
{
	if(*((char *)0x1FC80000 - 0xAE) == 'E') return(1);
	return(0);
}

//---------------------------------------------------------------------------
void gs_wait_hsync(void)
{
	*CSR = *CSR & 4;
	while(!(*CSR & 4));
}

